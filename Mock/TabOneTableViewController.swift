    // MARK: - Navigation

    // In a storyboard-based application, you will often want to do a little preparation before navigation
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        if segue.identifier ?? "" == "Detail" {
            guard let tabOneDetailsViewController = segue.destination as? TabOneDetailsViewController else {
                fatalError("Unexpected destination: \(segue.destination)")
            }
            
            guard let selectedMealCell = sender as? UITableViewCell else {
                fatalError("Unexpected sender: \(String(describing: sender))")
            }
            
            guard let indexPath = tableView.indexPath(for: selectedMealCell) else {
                fatalError("The selected cell is not being displayed by the table")
            }
            
            let user = GlobalData.sharedData.data[indexPath.row]
            tabOneDetailsViewController.name = user.name
            tabOneDetailsViewController.code = user.code
            tabOneDetailsViewController.color = user.color
        }
    }

